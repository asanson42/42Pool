void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int tmp[size];

	i = 0;
	j = size - 1;
	while (i < size)
	{
		tmp[i] = tab[i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = tmp[j];
		i++;
		j--;
	}
}
